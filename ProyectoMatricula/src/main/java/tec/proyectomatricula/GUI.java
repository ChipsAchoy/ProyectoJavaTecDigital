    private void RegistrarEscuelabuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarEscuelabuttonActionPerformed
        
        //Revisar si los campos estan vacios mostrar un joptionpane
        if(NombreField.getText().isEmpty() || CodigoField.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Los campos no pueden estar vacios", "Warning", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
        //Revisar si ya existe el codigo
        else if(escuelaController.getEscuelasArea().stream().anyMatch((escuela) -> escuela.getCode().equals(CodigoField.getText()))){
            javax.swing.JOptionPane.showMessageDialog(this, "Ya existe una escuela o area con ese codigo", "Warning", javax.swing.JOptionPane.WARNING_MESSAGE);
        //revisar que el codigo sea de 2 letras
        }else if(CodigoField.getText().length() != 2){
            javax.swing.JOptionPane.showMessageDialog(this, "El código debe ser de 2 caracteres", "Warning", javax.swing.JOptionPane.WARNING_MESSAGE);

        }else{
            //Crear una nueva escuela
            escuelaController.addEscuelaArea(new EscuelaArea(NombreField.getText(), CodigoField.getText()));
            javax.swing.JOptionPane.showMessageDialog(this, "Escuela o Area registrada con exito");
            //Limpiar los campos
            NombreField.setText("");
            CodigoField.setText("");

            Escuela_to_menu();

        }
    }//GEN-LAST:event_RegistrarEscuelabuttonActionPerformed

