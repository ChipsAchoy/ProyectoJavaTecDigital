    private void RegisterCursobuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterCursobuttonActionPerformed
        // TODO add your handling code here:
        //Revisar si los campos estan vacios mostrar un joptionpane
        if(NombreCursoField.getText().isEmpty() || RegCursoCodField.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Los campos no pueden estar vacios", "Warning", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
        //check codigo es de 6 caracteres y es unico
        else if(RegCursoCodField.getText().length() != 4){
            javax.swing.JOptionPane.showMessageDialog(this, "El código debe ser de 4 caracteres", "Warning", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
        else if(cursoController.getCursos().stream().anyMatch((curso) -> Integer.toString(curso.getCode()).equals(RegCursoCodField.getText()))){
            javax.swing.JOptionPane.showMessageDialog(this, "Ya existe un curso con ese codigo", "Warning", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
        else{
            //Crear un nuevo curso
            Curso curso = new Curso(escuelaController.getEscuelasArea().get(RegCursoEscuelabox.getSelectedIndex()), NombreCursoField.getText(), Integer.parseInt(RegCursoCodField.getText()), Integer.parseInt(RegCursoCredbox.getSelectedItem().toString()), Integer.parseInt(RegCursoHorasbox.getSelectedItem().toString()));
            cursoController.addCurso(curso);
            javax.swing.JOptionPane.showMessageDialog(this, "Curso registrado con exito");
            //Limpiar los campos
            NombreCursoField.setText("");
            RegCursoCodField.setText("");

            //print curso data
            System.out.println("Curso: " + curso.getName() + " Codigo: " + curso.getCode() + " Creditos: " + curso.getCreditos() + " Horas: " + curso.getHorasLectivas() + " Escuela: " + curso.getEscArea().getName());

            Curso_to_menu();
        }
    }//GEN-LAST:event_RegisterCursobuttonActionPerformed

